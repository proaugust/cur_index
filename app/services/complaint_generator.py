def generate_logistics_complaints(count: int = _COUNT) -> list[str]:
    results: list[str] = []
    seen: set[str] = set()

    while len(results) < count:
        template = _RANDOM.choice(_TEMPLATES)
        text = template.format(
            s=_RANDOM.choice(_SUBJECTS),
            d=_RANDOM.choice(_DAYS),
            a=_RANDOM.choice(_ACTIONS),
            c=_RANDOM.choice(_COLLOQUIAL),
        )
        text = text.replace("，，", "，").strip("，")
        if text in seen:
            continue
        seen.add(text)
        results.append(text)

    return results
